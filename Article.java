package dblp;

public class Article 
{
	private String pages;
	private String volume;
	private String journal;
	private String ee;
	
	public String getPages()
	{
		return this.pages;
	}
	public String getVolume()
	{
		return this.volume;
	}
	public String getJournal()
	{
		return this.journal;
	}
	public String getEE()
	{
		return this.ee;
	}
}
